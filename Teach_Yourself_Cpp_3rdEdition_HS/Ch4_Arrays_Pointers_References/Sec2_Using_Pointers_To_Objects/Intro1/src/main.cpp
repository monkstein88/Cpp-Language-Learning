
/*
	Intro 1.: As discussed in Chapter 2, objects can be accessed via pointers. As you know, when a pointer
  to an object is used, the object's members are referenced using the arrow (->) operator instead of the
  dot (.) operator.

  Pointer arithmetic using an object pointer is the same as it is for any other data type: it is performed
  relative to the type of the object. For example, when an object pointer is incremented, it points to the
  next object. When an object pointer is decremented, it points to the previous object.
*/
