
/*
  Exercise 4.: When is it appropriate to use default arguments? When is it probably a bad idea?
*/


/*
	My answer:
		- It can be usefull when you want to have somewhat of appropriate/acceptable function parameters by default, best case,
		if not if the function is called without an argument for that particular parameter.
		- It can help reduces ambiguity and writing of code and readibility - not needing to write a separate overloading function
		each specific case, when one or number of the parameters of a function are being called.

		- It is bad idea to do it too much, as that will lead to ambiguity - i.e. it is not necessary to make every function with
		a default value for each specific case

	Official answer:
		-  It is appropriate to use default arguments when there are one or more values that will
			 occur frequently. Is is inappropriate when there is no value or values that have a greater
   		 likelihood of occurring.
*/
