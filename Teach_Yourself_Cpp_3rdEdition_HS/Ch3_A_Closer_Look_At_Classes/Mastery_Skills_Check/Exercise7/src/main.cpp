
/*
	Exercise 7.: What is a friend function?

	A friend function that is declared inside an object, but actually is not a nonmember (of the object) function,
	that is granted access to the private members of the class for which it is a friend.

	It is most commonly used in operator overloading code, the creation of certain types of I/O function,
	and in cases where we want one function to have private access to the members of several different
	classes (even if they types are different)
*/
