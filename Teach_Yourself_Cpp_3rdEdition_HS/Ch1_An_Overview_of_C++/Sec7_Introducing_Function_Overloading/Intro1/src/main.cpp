
/*
	Intro 1.: After classes, perhaps the next most important and pervasive C++ feature is 'function overloading'. Not only does function
	overloading provide the mechanism by which C++ achieves one type of polymorphism, it also forms the basis by which the C++ programming
	environment can be dynamically extended. Because of the importance of overloading, a brief discussion is given here.

	In C++, two or more functions can share the same name as long as either the type of their arguments differs or the number of their
	arguments differs - or both. When two or more functions share the same name, they are said to be 'overloaded'. Overloaded functions can
	help reduce the complexity of a program by allowing related operations to be referred to by the same name.

	It is very easy to overload afunction: simply declare and define all required versions. The compiler will automatically select the
	correct version based upon the number and/or type of the arguments used to call the function.

	Note: It is also possible in C++ to overload operators. However, before you can fully understand operator overloading, you will need to
	know more about C++.
*/
